
public class Eat {
	private int eater, food, jumpTo;
	public Eat(int eater, int food, int jumpTo) {
		this.eater = eater;
		this.food = food;
		this.jumpTo = jumpTo;
	}
	
	public int getEater() {
		return eater;
	}
	
	public int getFood() {
		return food;
	}
	
	public int getJumpTo() {
		return jumpTo;
	}
	
	
}
